<section class="faq-container">
	<p><b> Background:</b>
Increasing number of vehicles on road is causing huge traffic jams and severely
affecting the environment, productivity and the economy. People who are conscious
and want to contribute in improving the situation, or otherwise want to avoid driving in
congested roads, would like to find alternatives like carpooling and ridesharing. The
challenge in traditional carpooling is it only works within fixed group of people who have
near perfect coordination and understanding in terms of matching timings, and
matching start and end points. However in today’s work environments with dynamic
requirements and flexible timings, finding even one stable carpooling partner is very
difficult.</p>
<p> <b>Need: </b>There clearly exists the need of a system that can connect people working at the
same company (or building or tech-park) and travelling along same route(s). It should
allow users to connect dynamically based on matching route and timing with the
objective of sharing a vehicle. This behaviour, when adopted by sufficiently large
number of people, will help decongest roads by reducing number of vehicles in the long
run.</p>
<p>Only if ride-sharing/carpooling survives the initial days, and manages to attract a
sufficiently large user base within a geographical area – it can survive and thrive to a
level where the long term benefits of less congested roads, lower carbon footprint and
greener environment become a reality.</p>
<p>For a massive multi-user community that is necessary for carpooling to become
sustainable, only an automated tracking and recording system of ride-share credits, with
a common basis of calculation, would help in keeping track of each user’s contribution
to the cause of carpooling.</p>
<p><b> Constraint: </b> As per Indian Motor Vehicles Act, private vehicles cannot carry passengers
for commercial gains. Hence Car Owners and Co-Travelers are strictly limited by Law to
share a private vehicle in a private capacity, and any other ‘Services’ are strictly
forbidden. The Car Owner must not provide any additional services to the Co-Traveler in
exchange for hiring charges.</p>
<p><b> Solution :</b> In view of the need, and keeping the constraints in consideration, Share-My-Cab
has devised a solution. she/he gets Share-A-Cab ‘ride points’ that can be used to avail a ride
from someone else who is also part of this network. These points are strictly
restricted for use - either availing shared rides from others, or (as of now) for
exchanging for fuel  </p>
